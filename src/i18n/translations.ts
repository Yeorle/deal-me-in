const dictionaries: Record<Language, Record<TranslationKey, string>> = { en, fr };

export function translate(language: Language, key: TranslationKey, vars?: Record<string, string | number>): string {
    const dict = dictionaries[language] ?? en;
    let value: string = dict[key] ?? en[key] ?? key;
    if (vars) {
        for (const [k, v] of Object.entries(vars)) {
            value = value.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
        }
    }
    return value;
}
